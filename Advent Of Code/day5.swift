	func part1() -> (Int, [Int]) {
		var seats: [Int] = []
		
		for line in input.trimmingCharacters(in: .whitespacesAndNewlines).components(separatedBy: "\n") {
			let parts: [String] = line.map { e in String(e) }
			let rows = parts[0...6]
			let columns = parts[7...9]
			
			var row: [Int] = [0, 127]
			var column: [Int] = [0, 7]
			
			for r in rows {
				let range: Int = row.max()! / 2
				let d: Int = (row.min()! + row.max()!) / 2
				var new_column: [Int]
				
				if r == "F" {
					if row[0] == 0 {
						new_column = [row.min()!, range]
					} else {
						new_column = [row.min()!, d]
					}
					
					row = new_column
				} else if r == "B" {
					row = [d + 1, row.max()!]
				}
			}
			
			for c in columns {
				let range: Int = column.max()! / 2
				let d: Int = (column.min()! + column.max()!) / 2
				var new_column: [Int]
				
				if c == "L" {
					if column[0] == 0 {
						new_column = [column.min()!, range]
					} else {
						new_column = [column.min()!, d]
					}
					
					column = new_column
				} else if c == "R" {
					column = [d + 1, column.max()!]
				}
			}

			seats.append(row.min()! * 8 + column.max()!)
		}
		
		return (seats.max()!, seats)
	}
	
